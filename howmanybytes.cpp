#include <iostream>
using namespace std;
main()
{
  cout << "number of bytes in short = " << sizeof(short) << '\n';
  cout << "number of bytes in long int = " << sizeof(long) << '\n';
  cout << "number of bytes in int = " << sizeof(int) << '\n';
  cout << "number of bytes in unsigned int = " << sizeof(unsigned int) << '\n';
}
